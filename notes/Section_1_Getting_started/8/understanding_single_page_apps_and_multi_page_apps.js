/*
    React (Angualr and Vue too) can build 2 kinds of web apps:

        #  single page apps (content is rendered in the client side)
            server returns only 1 page when the user visits the app page

            everything else is managed with JS (with React)
                content is rendered in the client side

            page consists of components (rendered by JS)
                *only one call a ReactDOM.render()


                

        #  multi-page apps (content is rendered by the server side)
            Multiple pages

            React can be used but only for little widgets

            Pages are normal HTML and CSS + some widgets (possibly managed by react)
                *widgets will each call a ReactDOM.render()

            
*/
